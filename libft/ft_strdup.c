#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t			i;
	unsigned char	*new;

	i = 0;
	new = malloc(sizeof (char) * ft_strlen(s1) + 1);
	if (!new)
		return (0);
	while (s1[i] != '\0')
	{
		new[i] = s1[i];
		i++;
	}
	new[i] = '\0';
	return ((char *)new);
}
